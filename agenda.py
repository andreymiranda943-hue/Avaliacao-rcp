def agenda(nome, telefone):
    """
    Retorna uma string formatada contendo nome e telefone.

    Parâmetros:
        nome (str): Nome da pessoa.
        telefone (str): Número de telefone no formato (99999-9999).

    Retorna:
        str: String no formato 'Contato: Nome - Telefone'.
    """
    return f"Contato: {nome} - {telefone}"
